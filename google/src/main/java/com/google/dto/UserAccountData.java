package com.google.dto;

public class UserAccountData {
	
	private String gmailId;
	private String fullName;
	private int defaultMemoryInGB;
	
	
	
	public String getGmailId() {
		return gmailId;
	}
	public void setGmailId(String gmailId) {
		this.gmailId = gmailId;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public int getDefaultMemoryInGB() {
		return defaultMemoryInGB;
	}
	public void setDefaultMemoryInGB(int defaultMemoryInGB) {
		this.defaultMemoryInGB = defaultMemoryInGB;
	}
	
	
	

}
